/// Spacing constants for consistent layout spacing across the app
class TopSpacing {
  TopSpacing._();

  // Base spacing values (in pixels)
  static const double xs = 4.0;
  static const double sm = 8.0;
  static const double md = 16.0;
  static const double lg = 24.0;
  static const double xl = 32.0;
  static const double xxl = 48.0;
  static const double xxxl = 64.0;

  // Common spacing values with semantic names
  static const double none = 0.0;
  static const double tiny = xs; // 4
  static const double small = sm; // 8
  static const double medium = md; // 16
  static const double large = lg; // 24
  static const double extraLarge = xl; // 32
  static const double huge = xxl; // 48
  static const double massive = xxxl; // 64

  // Specific use case spacing
  static const double buttonPaddingVertical = 12.0;
  static const double buttonPaddingHorizontal = 24.0;
  static const double inputPaddingVertical = 12.0;
  static const double inputPaddingHorizontal = 16.0;
  static const double cardPadding = 16.0;
  static const double screenPadding = 16.0;
  static const double listItemPadding = 16.0;

  // Border radius values
  static const double radiusXs = 4.0;
  static const double radiusSm = 8.0;
  static const double radiusMd = 12.0;
  static const double radiusLg = 16.0;
  static const double radiusXl = 24.0;
  static const double radiusFull = 9999.0;
}
